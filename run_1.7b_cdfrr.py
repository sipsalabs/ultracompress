# Verify baseline matches record baseline: time MLP outputs zeros → γ=1, β=0, τ=1
# But the pretrained block was trained with non-trivial scale_gamma/scale_beta.
# So baseline will be LOWER than pure_kl baseline. That's fine — it's the true
# novelty: we're replacing learned per-scale γ/β with a smooth function.
# The model will have to re-learn γ/β via the time MLP. Init with a warm start:
# set time MLP to approximately reproduce the pretrained scale_gamma/scale_beta.

# Warm start: fit time MLP final layer to output the pretrained scale values
# at the 28 layer points (one-shot least-squares is too complex; instead just
# randomize the final layer a tiny bit so it can diverge from zero quickly).
# Pretrained has scale_gamma (4 scales, D) repeated 7x → 28 layers.
# We'll use the "correct" per-layer targets for init supervision later if needed.
with torch.no_grad():
    scale_gamma = ckpt['scale_gamma']   # (4, D)
    scale_beta = ckpt['scale_beta']     # (4, D)
    iter_scale = ckpt['iter_scale']     # (4, 7)

    # Build per-layer targets
    target_gamma = torch.zeros(model.total_layers, hidden, device=DEVICE)
    target_beta = torch.zeros(model.total_layers, hidden, device=DEVICE)
    target_tau = torch.zeros(model.total_layers, device=DEVICE)
    for s in range(N_SCALES):
        for it in range(ITERS_PER_SCALE):
            li = s * ITERS_PER_SCALE + it
            target_gamma[li] = scale_gamma[s]
            target_beta[li] = scale_beta[s]
            target_tau[li] = iter_scale[s, it]

# Copy pretrained per-scale γ/β/τ into the BASE params. Time MLP adds residual.
