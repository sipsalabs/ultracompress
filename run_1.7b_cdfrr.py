class ContinuousDepthFRR(nn.Module):
    """FRR where layer-specific params (γ, β, τ=iter_scale) are generated by
    a shared time-conditioned MLP. No stored per-layer parameters.

    Depth t_ℓ = ℓ / (L-1) ∈ [0, 1].
    Time embedding: sin/cos at learnable frequencies.
    """
    def __init__(self, hidden_dim, n_heads, n_scales, iters_per_scale,
                 vocab_size, ff_mult, n_freq, time_mlp_hidden,
                 embed_weight=None, lm_head_weight=None, norm_weight=None):
        super().__init__()
        self.hidden_dim = hidden_dim
        self.n_scales = n_scales
        self.iters_per_scale = iters_per_scale
        self.total_layers = n_scales * iters_per_scale
        self.n_freq = n_freq

        # Shared block (the workhorse, 29M params)
        self.block = FractalBlock(hidden_dim, n_heads, ff_mult)

        # Per-layer BASE γ/β/τ (initialized from pretrained scales so baseline
        # matches record; the time MLP adds a learned smooth residual on top).
        self.gamma_base = nn.Parameter(torch.ones(self.total_layers, hidden_dim))
        self.beta_base = nn.Parameter(torch.zeros(self.total_layers, hidden_dim))
        self.tau_base = nn.Parameter(torch.ones(self.total_layers))

        # LEARNABLE frequencies for time encoding. Keep them modest — the 28
        # layer points only carry frequencies up to ~14 before aliasing.
        # Init: 1, 2, 3, ..., n_freq (linear geometric sense, not 2^k).
        freq_init = torch.arange(1, n_freq + 1).float()    # [1, 2, ..., n_freq]
        self.freqs = nn.Parameter(freq_init)
        # Phases per frequency (shift sin vs cos)
        self.phases = nn.Parameter(torch.zeros(n_freq))

        time_emb_dim = 2 * n_freq    # sin + cos per freq

        # Time MLP: maps time_emb → (γ, β, τ_scalar)
        # Output dim = 2*D + 1
        self.time_mlp = nn.Sequential(
            nn.Linear(time_emb_dim, time_mlp_hidden),
            nn.SiLU(),
            nn.Linear(time_mlp_hidden, time_mlp_hidden),
            nn.SiLU(),
            nn.Linear(time_mlp_hidden, 2 * hidden_dim + 1),
        )
        # Init final layer to 0 so γ ≈ 1, β ≈ 0, τ ≈ 1 at init
        # (residual wrt the *base* values γ=1, β=0, τ=1)
        nn.init.zeros_(self.time_mlp[-1].weight)
        nn.init.zeros_(self.time_mlp[-1].bias)

        # Precompute the 28 time points (register as buffer, fixed)
        t = torch.linspace(0.0, 1.0, self.total_layers)    # [0, 1/27, 2/27, ..., 1]
        self.register_buffer('t_points', t)

        # Frozen embed / lm_head / final norm
        if embed_weight is not None:
            self.embed = nn.Embedding.from_pretrained(embed_weight, freeze=True)
        else:
            self.embed = nn.Embedding(vocab_size, hidden_dim)
        self.lm_head = nn.Linear(hidden_dim, vocab_size, bias=False)
        if lm_head_weight is not None:
            self.lm_head.weight = nn.Parameter(lm_head_weight, requires_grad=False)
        self.norm = nn.RMSNorm(hidden_dim)
        if norm_weight is not None:
            self.norm.weight = nn.Parameter(norm_weight, requires_grad=False)

    def time_embedding(self, t):
        """t: (L,) scalar times in [0,1] → (L, 2F) sinusoidal features."""
        # phase: (L, F) = 2π·freqs·t + phases
        phase = 2 * math.pi * self.freqs[None, :] * t[:, None] + self.phases[None, :]
        return torch.cat([torch.sin(phase), torch.cos(phase)], dim=-1)   # (L, 2F)

    def compute_layer_params(self, depth_multiplier=1.0):
        """Produce γ, β, τ for each virtual layer.

        γ_ℓ = γ_base[ℓ] + MLP_residual(time_emb(t_ℓ))

        This preserves the pretrained baseline exactly (MLP inits to zero)
        while giving the time MLP a smooth residual capacity to learn. At
        inference, depth_multiplier allows continuous-depth evaluation.
        """
        t = self.t_points * depth_multiplier
        time_emb = self.time_embedding(t)       # (L, 2F)
        params = self.time_mlp(time_emb)        # (L, 2D+1)
        D = self.hidden_dim
        gamma_delta = params[:, :D]             # (L, D) init=0
        beta_delta = params[:, D:2*D]           # (L, D) init=0
        tau_delta = params[:, 2*D]              # (L,)   init=0
        gamma = self.gamma_base + gamma_delta
        beta = self.beta_base + beta_delta
        tau = self.tau_base + tau_delta
        return gamma, beta, tau

    def forward(self, tokens):
        x = self.embed(tokens).float()
        gamma_all, beta_all, tau_all = self.compute_layer_params()

        layer_idx = 0
        for scale in range(self.n_scales):
            for it in range(self.iters_per_scale):
                gamma = gamma_all[layer_idx]
                beta = beta_all[layer_idx]
                tau = tau_all[layer_idx]
                block_out = self.block(x, gamma, beta)
                x = x + (block_out - x) * tau
                layer_idx += 1

        x = self.norm(x)
        return self.lm_head(x)

    def block_params(self):
        return sum(p.numel() for p in self.block.parameters())

    def time_params(self):
        return (
            sum(p.numel() for p in self.time_mlp.parameters())
            + self.freqs.numel() + self.phases.numel()
        )

    def base_param_count(self):
        return (self.gamma_base.numel() + self.beta_base.numel()
                + self.tau_base.numel())


