"""
1.7B CONTINUOUS-DEPTH FRR (CD-FRR) — NOVEL ARCHITECTURE

PROBLEM: All per-layer static-parameter variants (LoRA, dual-block, MBBR,
frozen-block) hit the SAME ceiling at ~68% last-tok T10 (1000-sample hires):
  pure_kl step 5000: 68.23% [67.36, 69.11]
  lora_final:        68.15% [67.30, 69.10]
  lora_best:         67.67% [66.82, 68.56]

All these share one assumption: store a per-layer parameter, look it up at
layer ℓ. That's just rank-1 per-layer indexing. The 68% ceiling may be the
fundamental info limit of that family.

GENUINELY NEW IDEA — CONTINUOUS-DEPTH FRR:
Steal the core trick from diffusion models / NeRF: treat layer depth as
CONTINUOUS TIME t ∈ [0,1] and encode it with SINUSOIDAL positional features.
All layer-specific parameters (γ, β, iter_scale) become OUTPUTS OF A TINY
MLP that reads time embeddings:

  t_ℓ = ℓ / (L - 1)
  time_emb = [sin(2π·f_k·t), cos(2π·f_k·t)]_{k=1..F}   # F=16 frequencies
  [γ_ℓ, β_ℓ, τ_ℓ] = MLP_θ(time_emb(t_ℓ))

Novelty beyond prior work:
  1. NO per-layer stored parameters. γ, β are FUNCTIONS of depth.
  2. CONTINUOUS depth: train at L=28, infer at L=56/14/112 by resampling t.
     Nobody does this in distillation literature.
  3. SMOOTHNESS PRIOR: sinusoidal encoding naturally biases toward smooth
     functions of depth (well-known in NeRFs). Inductive bias > more params.
  4. COMPRESSION: 29M block + 0.2M time-MLP = 29.2M. Same 27.9x compression
     as the pure_kl record, but with the new capability.

COMBINED WITH PURE KL TRAINING (confirmed winning recipe).

Init strategy:
  - Block: pretrained from frr_1.7b_100k_final.pt (same as record)
  - Time MLP's final layer: zero → γ≈1, β≈0, τ≈1 at init → forward == baseline
  - Plus a per-frequency learnable amplitude (easier optimization)

Runs on GPU 0 only. Single-process. Kill switch: save best checkpoint each
eval, if no improvement for 5k steps → stop early.
"""
